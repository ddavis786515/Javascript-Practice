let fruits = ['Apple', 'Banana', 'Lemon'];
console.log(fruits);
//pop removes the last element in anarray
fruits.pop();
//shift removes the first element in the array
fruits.shift();
console.log(fruits);
//push adds element to end of array
fruits.push('Orange');
//unshift adds element to beginning of array
fruits.unshift('Tomato');
console.log(fruits);
